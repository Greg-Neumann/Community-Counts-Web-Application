            return View("RoleManager");
        }
        // Delete role for an existing User
        [HttpPost]
        [ValidateAntiForgeryToken]
        [Authorize(Roles = "systemAdmin")]
        public ActionResult RoleDeleteForAUser(string UserName, string RoleName)
        {
            var context = new ApplicationDbContext();  // Get Identity database context
            ApplicationUser user = context.Users.Where(u => u.UserName.Equals(UserName, StringComparison.CurrentCultureIgnoreCase)).FirstOrDefault();

            if (this.UserManager.IsInRole(user.Id, RoleName))
            {
                this.UserManager.RemoveFromRole(user.Id, RoleName);
                ViewBag.ResultMessage = "Role removed from this user successfully !";
            }
            else
            {
                ViewBag.ResultMessage = "This user doesn't belong to selected role.";
            }
            // prepopulat roles for the view dropdown
            var list = context.Roles.OrderBy(r => r.Name).ToList().Select(rr => new SelectListItem { Value = rr.Name.ToString(), Text = rr.Name }).ToList();
            ViewBag.Roles = list;

            return View("RoleManager");
        }
